import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const mode = process.argv[2] ?? "production";
const root = dirname(fileURLToPath(import.meta.url));

const urls = {
  production: "https://kioskpos.shop",
  development: "https://kioskpos.shop",
  local: "http://10.0.2.2:3000",
  "local-ios": "http://localhost:3000",
};

const serverUrl = process.env.CAPACITOR_SERVER_URL ?? urls[mode] ?? urls.production;
const isLocalDev = serverUrl.startsWith("http://");
const hostname = new URL(serverUrl).hostname;

const allowNavigation = [
  hostname,
  "*.kioskpos.shop",
  "kioskpos.shop",
  "accounts.google.com",
  "*.google.com",
  "*.googleusercontent.com",
  "*.supabase.co",
  "supabase.co",
  "10.0.2.2",
  "play.google.com",
  "apps.apple.com",
  "itms-apps",
];

const config = `import type { CapacitorConfig } from "@capacitor/cli";

/** Generated by scripts/write-capacitor-config.mjs (${mode}) */
const config: CapacitorConfig = {
  appId: "shop.kioskpos.app",
  appName: "Kiosk POS",
  webDir: "www",
  backgroundColor: "#1c1f26",
  server: {
    url: "${serverUrl}",
    cleartext: ${isLocalDev},
    hostname: "${hostname}",
    androidScheme: "${isLocalDev ? "http" : "https"}",
    allowNavigation: ${JSON.stringify(allowNavigation)},
    errorPath: "index.html",
  },
  android: {
    allowMixedContent: ${isLocalDev},
    backgroundColor: "#1c1f26",
    appendUserAgent: "KioskPOS-Native/1.0",
  },
  ios: {
    contentInset: "automatic",
    backgroundColor: "#1c1f26",
    appendUserAgent: "KioskPOS-Native/1.0",
    scrollEnabled: true,
    scheme: "App",
  },
  plugins: {
    SplashScreen: {
      launchAutoHide: false,
      launchShowDuration: 0,
      backgroundColor: "#1c1f26",
      androidSplashResourceName: "splash",
      showSpinner: true,
      spinnerColor: "#f97316",
    },
    StatusBar: {
      style: "DARK",
      backgroundColor: "#1c1f26",
    },
    Keyboard: {
      resize: "body",
      resizeOnFullScreen: true,
    },
    LocalNotifications: {
      smallIcon: "ic_launcher_foreground",
      iconColor: "#f97316",
      sound: "default",
    },
    Camera: {
      permissions: ["camera"],
    },
  },
};

export default config;
`;

writeFileSync(join(root, "..", "capacitor.config.ts"), config, "utf8");
console.log(`Wrote capacitor.config.ts for mode=${mode} → ${serverUrl}`);
